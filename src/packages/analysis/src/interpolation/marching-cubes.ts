// ============================================================
// analysis/interpolation/marching-cubes.ts — Marching Cubes 3D 等值面
// ============================================================
//
// 提取 3D 标量场的等值面（iso-surface）为三角网格。
// 3D 版本的 Marching Squares。地形、医学体数据、流体可视化的标准算法。
//
// 算法（Lorensen & Cline 1987）：
// 1. 逐个遍历体素（cube），计算 8 个角点相对等值 iso 的符号（0/1 bit）。
// 2. 8 bit 组成 cubeIndex ∈ [0, 256)。
// 3. 用预计算的 edgeTable[cubeIndex] 找出哪些边被等值面穿过。
// 4. 在被穿过的边上线性插值求交点。
// 5. 用预计算的 triTable[cubeIndex] 连接这些交点为三角形（0-5 个/体素）。
//
// 8 个角点的编号规则（遵循 Lorensen 原始论文）：
//      v4-------v5       y
//     / |      /|        |
//    v7-------v6 |       +---x
//    |  v0----|--v1     /
//    | /      | /      z
//    v3-------v2
//
// 12 条边的编号（从 v0 开始顺时针）：
//   底面：e0(v0→v1), e1(v1→v2), e2(v2→v3), e3(v3→v0)
//   顶面：e4(v4→v5), e5(v5→v6), e6(v6→v7), e7(v7→v4)
//   立柱：e8(v0→v4), e9(v1→v5), e10(v2→v6), e11(v3→v7)
//
// 全部 Float64 / Int32。
// ============================================================

/**
 * Marching Cubes 的结果：三角网格（扁平顶点数组 + 索引数组）。
 */
export interface IsoSurface {
    /** 顶点坐标 [x0,y0,z0, x1,y1,z1, ...]（Float64） */
    readonly vertices: Float64Array;
    /** 三角形顶点索引 [i0,i1,i2, ...]（Uint32） */
    readonly indices: Uint32Array;
    /** 三角形数量 */
    readonly triangleCount: number;
}

/**
 * 3D 标量场。
 *
 * @property data Float64 扁平数组，长度 = nx·ny·nz，索引为 (z·ny + y)·nx + x
 * @property nx  X 维度大小
 * @property ny  Y 维度大小
 * @property nz  Z 维度大小
 * @property origin 原点 [x0, y0, z0]（体素 (0,0,0) 的世界坐标）
 * @property spacing 体素尺寸 [dx, dy, dz]
 */
export interface ScalarField3D {
    readonly data: Float64Array | Float32Array;
    readonly nx: number;
    readonly ny: number;
    readonly nz: number;
    readonly origin?: readonly [number, number, number];
    readonly spacing?: readonly [number, number, number];
}

/**
 * 从 3D 标量场中提取等值面。
 *
 * @param field 输入标量场
 * @param iso   等值阈值
 * @returns 等值面网格
 */
export function marchingCubes(field: ScalarField3D, iso: number): IsoSurface {
    const { data, nx, ny, nz } = field;
    const [ox, oy, oz] = field.origin ?? [0, 0, 0];
    const [dx, dy, dz] = field.spacing ?? [1, 1, 1];

    if (nx < 2 || ny < 2 || nz < 2) {
        return {
            vertices: new Float64Array(0),
            indices: new Uint32Array(0),
            triangleCount: 0,
        };
    }

    // 动态增长的顶点/索引缓冲
    const vertsBuf: number[] = [];
    const indsBuf: number[] = [];

    // 复用的 12 条边交点数组
    const edgeVerts = new Float64Array(12 * 3);

    // 顶点索引：记录当前立方体每条边的全局顶点索引，避免边共享时重复
    // （简化版：不去重，每个体素边生成一个新顶点；后续可加边哈希去重）
    const sample = (x: number, y: number, z: number): number => {
        return data[(z * ny + y) * nx + x];
    };

    for (let z = 0; z < nz - 1; z++) {
        for (let y = 0; y < ny - 1; y++) {
            for (let x = 0; x < nx - 1; x++) {
                // 8 角点值
                const v0 = sample(x, y, z);
                const v1 = sample(x + 1, y, z);
                const v2 = sample(x + 1, y, z + 1);
                const v3 = sample(x, y, z + 1);
                const v4 = sample(x, y + 1, z);
                const v5 = sample(x + 1, y + 1, z);
                const v6 = sample(x + 1, y + 1, z + 1);
                const v7 = sample(x, y + 1, z + 1);

                // cubeIndex：8 位，第 i 位 = (v_i < iso)
                let cubeIndex = 0;
                if (v0 < iso) cubeIndex |= 1;
                if (v1 < iso) cubeIndex |= 2;
                if (v2 < iso) cubeIndex |= 4;
                if (v3 < iso) cubeIndex |= 8;
                if (v4 < iso) cubeIndex |= 16;
                if (v5 < iso) cubeIndex |= 32;
                if (v6 < iso) cubeIndex |= 64;
                if (v7 < iso) cubeIndex |= 128;

                const edges = EDGE_TABLE[cubeIndex];
                if (edges === 0) continue; // 整个立方体在等值面同一侧

                // 8 角点的世界坐标
                const wx0 = ox + x * dx;
                const wx1 = ox + (x + 1) * dx;
                const wy0 = oy + y * dy;
                const wy1 = oy + (y + 1) * dy;
                const wz0 = oz + z * dz;
                const wz1 = oz + (z + 1) * dz;

                // 计算 12 条边的交点（仅在 edges bit 为 1 时）
                if (edges & 1) interp(edgeVerts, 0, v0, v1, wx0, wy0, wz0, wx1, wy0, wz0, iso);
                if (edges & 2) interp(edgeVerts, 1, v1, v2, wx1, wy0, wz0, wx1, wy0, wz1, iso);
                if (edges & 4) interp(edgeVerts, 2, v2, v3, wx1, wy0, wz1, wx0, wy0, wz1, iso);
                if (edges & 8) interp(edgeVerts, 3, v3, v0, wx0, wy0, wz1, wx0, wy0, wz0, iso);
                if (edges & 16) interp(edgeVerts, 4, v4, v5, wx0, wy1, wz0, wx1, wy1, wz0, iso);
                if (edges & 32) interp(edgeVerts, 5, v5, v6, wx1, wy1, wz0, wx1, wy1, wz1, iso);
                if (edges & 64) interp(edgeVerts, 6, v6, v7, wx1, wy1, wz1, wx0, wy1, wz1, iso);
                if (edges & 128) interp(edgeVerts, 7, v7, v4, wx0, wy1, wz1, wx0, wy1, wz0, iso);
                if (edges & 256) interp(edgeVerts, 8, v0, v4, wx0, wy0, wz0, wx0, wy1, wz0, iso);
                if (edges & 512) interp(edgeVerts, 9, v1, v5, wx1, wy0, wz0, wx1, wy1, wz0, iso);
                if (edges & 1024) interp(edgeVerts, 10, v2, v6, wx1, wy0, wz1, wx1, wy1, wz1, iso);
                if (edges & 2048) interp(edgeVerts, 11, v3, v7, wx0, wy0, wz1, wx0, wy1, wz1, iso);

                // 根据 triTable 生成三角形
                const tri = TRI_TABLE[cubeIndex];
                for (let i = 0; tri[i] !== -1; i += 3) {
                    const e0 = tri[i];
                    const e1 = tri[i + 1];
                    const e2 = tri[i + 2];

                    const base = vertsBuf.length / 3;
                    vertsBuf.push(
                        edgeVerts[e0 * 3], edgeVerts[e0 * 3 + 1], edgeVerts[e0 * 3 + 2],
                        edgeVerts[e1 * 3], edgeVerts[e1 * 3 + 1], edgeVerts[e1 * 3 + 2],
                        edgeVerts[e2 * 3], edgeVerts[e2 * 3 + 1], edgeVerts[e2 * 3 + 2],
                    );
                    indsBuf.push(base, base + 1, base + 2);
                }
            }
        }
    }

    return {
        vertices: new Float64Array(vertsBuf),
        indices: new Uint32Array(indsBuf),
        triangleCount: indsBuf.length / 3,
    };
}

/** 在两个角点之间线性插值得到等值交点，写入 edgeVerts[slot*3..]。 */
function interp(
    edgeVerts: Float64Array,
    slot: number,
    va: number, vb: number,
    ax: number, ay: number, az: number,
    bx: number, by: number, bz: number,
    iso: number,
): void {
    let t: number;
    const diff = vb - va;
    if (Math.abs(diff) < 1e-20) {
        t = 0.5;
    } else {
        t = (iso - va) / diff;
        if (t < 0) t = 0;
        else if (t > 1) t = 1;
    }
    edgeVerts[slot * 3] = ax + t * (bx - ax);
    edgeVerts[slot * 3 + 1] = ay + t * (by - ay);
    edgeVerts[slot * 3 + 2] = az + t * (bz - az);
}

