/**
 * Reusable palette definition for thematic styling.
 */
export type StylePreset = {
  /** Unique preset id for persistence and dropdown selection. */
  id: string;
  /** User-visible preset name. */
  name: string;
  /** Ordered color stops as CSS color strings (hex/rgb); length ≥ 2 for ramps. */
  colors: string[];
  /** Palette semantics for UI (legend type and interpolation hints). */
  type: 'categorical' | 'sequential' | 'diverging';
};

/**
 * Result payload from a measurement tool (length, area, or elevation profile).
 */
export type MeasureResult = {
  /** Which measurement class was produced. */
  type: 'distance' | 'area' | 'profile';
  /** Primary numeric result (length, area, or profile distance along path) in `unit` or derived. */
  value: number;
  /** Unit label for `value` (e.g. `"m"`, `"km"`, `"m²"`, `"ft"`). */
  unit: string;
  /** Sampled or vertex coordinates; inner arrays are `[x, y]` or `[lng, lat]` per app CRS. */
  coordinates: number[][];
  /** Optional per-sample elevations in meters (orthometric or ellipsoidal per app setting) for profiles. */
  elevations?: number[];
};

/**
 * Aggregate tile pipeline counters for debug HUD / performance overlay.
 */
export type TileStats = {
  /** Count of tiles that finished loading successfully in the current window or session. */
  loaded: number;
  /** Count of tiles currently in flight (network + decode). */
  loading: number;
  /** Count of tiles resident in CPU/GPU cache and eligible for reuse. */
  cached: number;
  /** Count of tiles that failed after retries (network or decode). */
  errored: number;
};

/**
 * GPU memory attribution snapshot (budget vs usage by resource category).
 */
export type GPUMemoryInfo = {
  /** Estimated total GPU memory currently used by the engine, in bytes. */
  used: number;
  /** Configured or detected memory budget cap for the GPU resource manager, in bytes. */
  budget: number;
  /** Bytes attributed to texture objects (atlas, terrain, attachments). */
  textures: number;
  /** Bytes attributed to buffer objects (geometry, uniforms, staging). */
  buffers: number;
  /** Approximate memory attributed to pipeline objects if tracked; bytes. */
  pipelines: number;
};

/**
 * Legend symbol shape for auto-generated layer legends.
 */
export type LegendShape = 'square' | 'line' | 'circle' | 'gradient';

/**
 * One row in a generated legend (color swatch + label).
 */
export type LegendItemEntry = {
  /** CSS color string for the swatch or gradient key color. */
  color: string;
  /** Human-readable class or range label. */
  label: string;
  /** Visual shape of the legend glyph. */
  shape: LegendShape;
};

/**
 * Aggregated legend rows for one map layer (name + style-derived entries).
 */
export type LegendItem = {
  /** Layer display name from `LayerConfig.name`. */
  layerName: string;
  /** One or more legend rows (single fill, line classes, ramp stops, …). */
  entries: LegendItemEntry[];
};
